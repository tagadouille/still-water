package com.app.main.model.core;

/**
 * Couleurs utilisables pour identifier une équipe ou un élément du jeu.
 */
public enum Color {
    /** Rouge. */
    RED,
    /** Vert. */
    GREEN,
    /** Violet / pourpre. */
    PURPLE,
    /** Bleu. */
    BLUE,
    /** Aucune couleur (valeur neutre). */
    NO_COLOR;
}
